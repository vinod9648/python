# and
a = 5
b = 3
if a > 4 and b > 2:
    print("Both conditions are True")   # Output: Both conditions are True

# or
a = 5
b = 3
if a < 4 or b > 2:
    print("At least one condition is True")   # Output: At least one condition is True

# not
a = 5
if not(a > 4):
    print("The condition is False")   # Output: The condition is False
